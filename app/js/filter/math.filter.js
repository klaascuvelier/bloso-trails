//
// Function from JS Math library made available to Angular
//

// Ceil value
BlosoApp.filter('ceil', function ceilFiltering() {
    return function ceilFilter(input) {
        return Math.ceil(input);
    };
});

// Floor value
BlosoApp.filter('floor', function ceilFiltering() {
    return function ceilFilter(input) {
        return Math.ceil(input);
    };
});